class Ques17{
    public static void main(String args[]){
        Integer i = new Integer(178);
        System.out.println("Integer Instance to Byte : "+i.byteValue());
        System.out.println("Integer Instance to Short : "+i.shortValue());
        System.out.println("Integer Instance to Byte : "+i.intValue());
        System.out.println("Integer Instance to Long : "+i.longValue());
        System.out.println("Integer Instance to Byte : "+i.floatValue());
        System.out.println("Integer Instance to Double : "+i.doubleValue());
    }
}